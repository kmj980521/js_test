/**
 * Variable 선언하기  
 *
 * 1) var - 더 이상 쓰지 않는 방법. 
 * 2) let
 * 3) const
 */

var name = '마이 네임';
console.log(name);

var age = 32;
console.log(age);

let text = '텍스트1';
console.log(text);

/**
 * let과 var로 선언하면 
 * 값을 추후에 변경할 수 있다. 
 */

const text2 = '텍스트2';
console.log(text2);

