/**
 * Naming Conventions
 * 
 * 1) 일반적으로 영어(라틴문자)를 사용하며 문자와 숫자를 모두 사용할 수 있다.
 * 2) 특수기호는 언더스코어(_)와 달러표시($)를 사용할 수 있다.
 * 3) 숫자로 이름을 시작할 수 없다. 
 * 4) 키워드는 변수명으로 사용할 수 없다.
 */

let myName ='마이 네임';

var $name = '달러 네임';

const _name = '언더 네임';

